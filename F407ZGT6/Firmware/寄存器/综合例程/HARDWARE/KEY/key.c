//按键初始化函数
void KEY_Init(void)
{
    RCC->AHB1ENR|=1<<4;     //使能PORTE时钟
    GPIO_Set(GPIOE,PIN6,GPIO_MODE_IN,0,0,GPIO_PUPD_PU);	//PE6
}
//按键处理函数
//返回按键值
//mode:0,不支持连续按;1,支持连续按;
//0，没有任何按键按下
//1，KEY0按下
//2，KEY1按下
//3，KEY2按下
//4，KEY_UP按下 即WK_UP
//注意此函数有响应优先级,KEY0>KEY1>KEY2>KEY_UP!!
u8 KEY_Scan(u8 mode)
{
    static u8 key_up=1;//按键按松开标志
    if(mode)key_up=1;  //支持连按
    if(key_up&&(KEY0==0))
    {
        delay_ms(10);//去抖动
        key_up=0;
        if(KEY0==0)return 1;
    } else if(KEY0==1)key_up=1;
    return 0;// 无按键按下
}
